# Array For Python
array=[3,89,45,67,2,99,176]
print(array)

# To append a item in the Arrar
# It Adds The data at last only 1 data
array.append(False)
array.append(True)
print(array)

# To arrange in order
# false takes as value 0
# true takes as value 1
array.sort()
print(array)

# To Arrange in reverse Order
array.reverse()
print(array)

# To instert in (Position , Data)   
array.insert(5,91)
print(array)

# Pop out the data in the INdex or position of 3
array.pop(3)
print(array)

# REmove the given data
array.remove(99)
print(array)